package org.example;

/**
 * Represents a trend in XP or activity over time.
 */
public enum Trend {
    /**
     * Activity is improving (increasing over time).
     */
    IMPROVING,

    /**
     * Activity is stable (relatively constant).
     */
    STABLE,

    /**
     * Activity is declining (decreasing over time).
     */
    DECLINING
}

