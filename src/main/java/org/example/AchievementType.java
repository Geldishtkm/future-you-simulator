package org.example;

/**
 * Enumeration of achievement types that users can earn.
 */
public enum AchievementType {
    LEVEL_MILESTONE,      // Reached specific level milestones
    STREAK_MASTER,        // Long habit streaks
    CONSISTENCY_KING,     // High consistency scores
    GOAL_ACHIEVER,        // Completed goals
    XP_HARVESTER,          // Accumulated large amounts of XP
    WEEK_WARRIOR,          // Active for entire week
    MONTH_CHAMPION,        // Active for entire month
    BURNOUT_SURVIVOR,      // Maintained activity despite burnout risk
    HABIT_MASTER,          // Mastered multiple habits
    FIRST_STEPS            // First habit completion
}

